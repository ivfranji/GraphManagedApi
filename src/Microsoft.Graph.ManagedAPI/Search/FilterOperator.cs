namespace Microsoft.Graph.Search
{
    /// <summary>
    /// Filter operators.
    /// </summary>
    public enum FilterOperator
    {
        /// <summary>
        /// And.
        /// </summary>
        and,

        /// <summary>
        /// Or.
        /// </summary>
        or,

        /// <summary>
        /// Equals.
        /// </summary>
        eq,

        /// <summary>
        /// Not equals.
        /// </summary>
        ne,

        /// <summary>
        /// Greater than.
        /// </summary>
        gt,

        /// <summary>
        /// Greater or equal.
        /// </summary>
        ge,

        /// <summary>
        /// Less than.
        /// </summary>
        lt,

        /// <summary>
        /// Less or equal.
        /// </summary>
        le
    }
}
