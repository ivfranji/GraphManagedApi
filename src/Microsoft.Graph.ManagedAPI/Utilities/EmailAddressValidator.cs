namespace Microsoft.Graph.Utilities
{
    using System;
    using System.Text.RegularExpressions;

    /// <summary>
    /// Email address validator.
    /// </summary>
    internal static class EmailAddressValidator
    {
        // email address validator.
        private static Regex emailAddressValidator = new Regex(
            @"^((([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+(\.([a-z]|\d|[!#\$%&'\*\+\-\/=\?\^_`{\|}~]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])+)*)|((\x22)((((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(([\x01-\x08\x0b\x0c\x0e-\x1f\x7f]|\x21|[\x23-\x5b]|[\x5d-\x7e]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(\\([\x01-\x09\x0b\x0c\x0d-\x7f]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF]))))*(((\x20|\x09)*(\x0d\x0a))?(\x20|\x09)+)?(\x22)))@((([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|\d|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))\.)+(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])|(([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])([a-z]|\d|-|\.|_|~|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])*([a-z]|[\u00A0-\uD7FF\uF900-\uFDCF\uFDF0-\uFFEF])))\.?$",
            RegexOptions.Compiled | RegexOptions.IgnoreCase);

        /// <summary>
        /// Validate if particular string is valid email address.
        /// </summary>
        /// <param name="emailAddress"></param>
        /// <returns></returns>
        internal static bool IsValid(string emailAddress)
        {
            return EmailAddressValidator.emailAddressValidator.Match(emailAddress).Length > 0;
        }

        /// <summary>
        /// Throws <see cref="ArgumentException"/> if email address not valid.
        /// </summary>
        /// <param name="emailAddress">Email address to validate.</param>
        internal static void ThrowIfInvalid(string emailAddress)
        {
            if (!EmailAddressValidator.IsValid(emailAddress))
            {
                throw new ArgumentException("Specified string is not valid email address.", emailAddress);
            }
        }
    }
}
